# INTEGERS #

a= 28           # This is a perfect number
print(type(a))  # <class 'int'>
print(a)        # 28

# FLOATS #

b= 2.43434      # This is a float number
print(type(b))  # <class 'float'>
print(b)        # 2.43434

# COMPLEX C #
# i = raiz(-1)

z=2-6.1j        # This is a complex number
print(type(z))  # <class 'complex'>
print(z)        # (2-6.1j)      
print(z.real)   #2.0
print(z.imag)   #-6.1
